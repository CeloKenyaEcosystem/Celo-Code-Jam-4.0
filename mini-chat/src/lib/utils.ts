import { type ClassValue, clsx } from 'clsx';
import { twMerge } from 'tailwind-merge';

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export const shortAddress = (addr: string) =>
  addr.length > 10 && addr.startsWith('0x')
    ? `${addr.substring(0, 6)}...${addr.substring(addr.length - 4)}`
    : addr;

export const truncate = (str: any, length: any) => {
  if (!str) {
    return str;
  }
  if (str.length > length) {
    return `${str.substring(0, length - 3)}...`;
  }
  return str;
};


export const getLatestMessage = (messages: any) =>
  messages?.length ? messages[messages.length - 1] : null;


export function getLastTwoUppercase(input: string): string {
  const lastTwo = input.slice(-2);
  return lastTwo.toUpperCase();
}
